    <h2>Historia</h2>
    <p><strong>Infancia y juventud:</strong> Criada por una madre estricta y con un padre encarcelado por pertenecer a los Ballas, Sylvia creció entre la sobreprotección y la presión de ser “la hija buena”. Siempre fue la favorita en casa, lo que generó tensiones con su hermana mayor, Lynsay.</p>

    <p><strong>Huida y amor prohibido:</strong> A los 19 años, se enamoró de Selene, una mujer mayor y teniente de policía. Se marchó sin despedirse y vivió con ella durante cuatro años en otra ciudad. La relación, inicialmente intensa, se deterioró tras dos fracasos en tratamientos de fertilidad, y terminó en traición e infidelidad por parte de Selene. La ruptura dejó a Sylvia emocionalmente devastada.</p>

    <p><strong>Regreso y descenso:</strong> Volvió a Groove sin rumbo y con heridas internas. Rechazada emocionalmente por su madre y sorprendida al ver a Lynsay envuelta con los Ballas, intentó ayudarla a salir... y terminó involucrándose ella misma. Su desprecio por la autoridad, alimentado por su historia con Selene, la empujó a unirse a la banda.</p>

    <h2>Ficha policial</h2>
    <p><strong>Estado actual:</strong> Limpio</p>
    <p><strong>Delitos no registrados (sospechas no confirmadas):</strong></p>
    <ul>
        <li>Participación en intercambios ilegales de drogas junto a su expareja Selene.</li>
        <li>Complicidad en transporte y distribución de sustancias prohibidas.</li>
        <li>Presunto doble homicidio durante un intercambio fallido, en defensa propia según su versión, aunque jamás fue investigado.</li>
    </ul>
    <p><strong>Riesgo emocional:</strong> Alta tendencia a la autodestrucción. Comportamiento de riesgo frecuente, motivado por impulsividad y una sensación latente de vacío existencial.</p>

    <h2>Familiares</h2>
    <p><strong>Padre:</strong> Antiguo miembro de los Ballas, fue arrestado por cargos relacionados con crimen organizado y actualmente cumple condena en una prisión federal. Su influencia marcó profundamente la infancia de Sylvia y su visión del mundo.</p>

    <p><strong>Madre:</strong> Mujer reservada y estricta, desempeñó un rol autoritario en la crianza de sus hijas. Tras la marcha de Sylvia, su relación se volvió distante. Recientemente se ha mencionado que planea vender la casa familiar, lo que ha causado tensión emocional en ambas hermanas.</p>

    <p><strong>Lynsay Collins:</strong> Hermana mayor de Sylvia. Actualmente trabajan juntas en <em>Grove Customs</em>, lo que ha reavivado una relación que oscila entre la complicidad y los conflictos no resueltos del pasado.</p>

    <h2>Personalidad</h2>
    <ul>
        <li><strong>Fuerte pero quebrada:</strong> Aunque resiliente, sus traumas no están sanados y los arrastra consigo.</li>
        <li><strong>Impulsiva y emocional:</strong> No filtra sus acciones, lo que a menudo la pone en peligro.</li>
        <li><strong>Leal:</strong> En especial con su hermana Lynsay. Es capaz de todo por protegerla.</li>
        <li><strong>Rencorosa:</strong> Guarda resentimientos y no olvida fácilmente.</li>
        <li><strong>Culta y crítica:</strong> A pesar de su imagen “conspiranoica”, es muy inteligente y le gusta jugar con las creencias ajenas.</li>
        <li><strong>Sarcástica y provocadora:</strong> Disfruta generando reacciones, incluso mediante teorías absurdas.</li>
        <li><strong>Tendencias suicidas:</strong> A veces toma decisiones peligrosas porque siente que su vida no le pertenece.</li>
        <li><strong>Antipolicía:</strong> El pasado con Selene alimenta un rechazo visceral a todo lo relacionado con la ley.</li>
    </ul>

    <h2>Relaciones</h2>
    <ul>
        <li><strong>Lynsay Collins (hermana):</strong> Hermana, amiga, compañera. Su lazo es inquebrantable a pesar de todo.</li>
        <li><strong>Selene (expareja):</strong> Figura clave de su caída emocional. La traición de Selene aún marca su forma de amar y confiar.</li>
        <li><strong>Dai (amiga):</strong> Exiliada en Japón durante un tiempo. La reencuentra y le ofrece trabajo.</li>
        <li><strong>Frank (novio de Dai):</strong> Policía. Potencial fuente de conflicto futuro.</li>
        <li><strong>Alex:</strong> Amigo de Lynsay. Sylvia sospecha que hay algo entre ambos. Aún lo observa con cierta distancia.</li>
    </ul>


    <h2>Estilo y curiosidades</h2>
    <ul>
        <li><strong>Antes:</strong> Sylvia vestía con ropa ancha, oscura y discreta, intentando ocultar su cuerpo y evitar llamar la atención.</li>
        <li><strong>Ahora:</strong> Ha comenzado a vestir colores vivos, especialmente el morado —símbolo de los Ballas— como acto de liberación y rebeldía contra su madre.</li>
        <li><strong>Comportamiento social:</strong> Le gusta extender cadenas falsas y bulos por diversión, jugando con la ingenuidad ajena.</li>
        <li><strong>Lugar seguro:</strong> Tiene un rincón específico detrás del sofá en su apartamento donde se siente más protegida.</li>
        <li><strong>Frase recurrente:</strong> “¿No os apetece un bocata de lomo queso?”.</li>
        <li><strong>Contradicciones:</strong> Aunque es culta, finge ser crédula para poner a prueba a los demás. Su sarcasmo es un mecanismo de defensa.</li>
    </ul>

