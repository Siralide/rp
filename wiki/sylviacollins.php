    <h2>Biografía</h2>
    <ul>
        <li><strong>Nombre completo:</strong> Sylvia Collins</li>
        <li><strong>Fecha de nacimiento:</strong> 24 de septiembre de 2002</li>
        <li><strong>Lugar de origen:</strong> Groove Street, Los Santos</li>
        <li><strong>Edad:</strong> 22 años (en 2025)</li>
        <li><strong>Interpretada por:</strong> Violeta</li>
        <li><strong>Afiliación:</strong> Ballas</li>
